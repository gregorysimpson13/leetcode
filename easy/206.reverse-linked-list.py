# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        left, right = None, head
        while right:
            tmp = right.next
            right.next = left
            right, left = tmp, right
        return left
# @lc code=end

